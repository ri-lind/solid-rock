package de.tum.cit.fop.maze.utilities;

public class ScalingFactor {

    public float width;
    public float height;

    public ScalingFactor(float width, float height){
        this.width = width;
        this.height = height;
    }

    public ScalingFactor() {
        this.width = 1;
        this.height = 1;
    }
}
